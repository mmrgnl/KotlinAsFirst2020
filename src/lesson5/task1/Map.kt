/**
 * Средняя (3 балла)
 *
 * Для заданного списка слов определить, содержит ли он анаграммы.
 * Два слова здесь считаются анаграммами, если они имеют одинаковую длину
 * и одно можно составить из второго перестановкой его букв.
 * Скажем, тор и рот или роза и азор это анаграммы,
 * а поле и полено -- нет.
 *
 * Например:
 *   hasAnagrams(listOf("тор", "свет", "рот")) -> true
 */
fun hasAnagrams(words: List<String>): Boolean {
    val wordDivide = mutableListOf<Set<Char>>().apply { words.forEach { this.add(it.toHashSet()) } }
    return wordDivide.groupingBy { it }.eachCount().any { it.value > 1 }
}

/**
 * Сложная (5 баллов)
 *
 * Для заданного ассоциативного массива знакомых через одно рукопожатие `friends`
 * необходимо построить его максимальное расширение по рукопожатиям, то есть,
 * для каждого человека найти всех людей, с которыми он знаком через любое
 * количество рукопожатий.
 *
 * Считать, что все имена людей являются уникальными, а также что рукопожатия
 * являются направленными, то есть, если Марат знает Свету, то это не означает,
 * что Света знает Марата.
 *
 * Оставлять пустой список знакомых для людей, которые их не имеют (см. EvilGnome ниже),
 * в том числе для случая, когда данного человека нет в ключах, но он есть в значениях
 * (см. GoodGnome ниже).
 *
 * Например:
 *   propagateHandshakes(
 *     mapOf(
 *       "Marat" to setOf("Mikhail", "Sveta"),
 *       "Sveta" to setOf("Marat"),
 *       "Mikhail" to setOf("Sveta"),
 *       "Friend" to setOf("GoodGnome"),
 *       "EvilGnome" to setOf()
 *     )
 *   ) -> mapOf(
 *          "Marat" to setOf("Mikhail", "Sveta"),
 *          "Sveta" to setOf("Marat", "Mikhail"),
 *          "Mikhail" to setOf("Sveta", "Marat"),
 *          "Friend" to setOf("GoodGnome"),
 *          "EvilGnome" to setOf(),
 *          "GoodGnome" to setOf()
 *        )
 */
fun propagateHandshakes(friends: Map<String, Set<String>>): Map<String, Set<String>> {
    val handshakeFriends = mutableMapOf<String, MutableSet<String>>()

    for ((p, f) in friends) {
        handshakeFriends[p] = mutableSetOf()
        val checkedFriends = mutableSetOf<String>()
        val toCheck = mutableSetOf<String>()

        toCheck.addAll(f)
        while (toCheck.isNotEmpty()) {
            val friend = toCheck.first()
            toCheck.remove(friend)
            if (checkedFriends.contains(friend) || friend == p)
                continue

            handshakeFriends[p]?.add(friend)
            checkedFriends.add(friend)

            if (friends.containsKey(friend))
                friends[friend]!!.let { toCheck.addAll(it) }
            else
                handshakeFriends[friend] = mutableSetOf()
        }
    }
    return handshakeFriends
}

/**
 * Сложная (6 баллов)
 *
 * Для заданного списка неотрицательных чисел и числа определить,
 * есть ли в списке пара чисел таких, что их сумма равна заданному числу.
 * Если да, верните их индексы в виде Pair<Int, Int>;
 * если нет, верните пару Pair(-1, -1).
 *
 * Индексы в результате должны следовать в порядке (меньший, больший).
 *
 * Постарайтесь сделать ваше решение как можно более эффективным,
 * используя то, что вы узнали в данном уроке.
 *
 * Например:
 *   findSumOfTwo(listOf(1, 2, 3), 4) -> Pair(0, 2)
 *   findSumOfTwo(listOf(1, 2, 3), 6) -> Pair(-1, -1)
 */
fun findSumOfTwo(list: List<Int>, number: Int): Pair<Int, Int> {
    val sortedList = mutableListOf<Pair<Int, Int>>()

    for (i in list.indices)
        if (list[i] <= number)
            sortedList.add(Pair(list[i], i))

    if (sortedList.size < 2)
        return Pair(-1, -1)
    sortedList.sortBy { it.first }

    var val1 = sortedList.first().first
    var val2 = sortedList.last().first
    var it1 = 0
    var it2 = sortedList.size - 1

    while (val1 + val2 != number) {
        if (val1 + val2 < number)
            val1 = sortedList[++it1].first
        else
            val2 = sortedList[--it2].first

        if (it1 == it2)
            return Pair(-1, -1)
    }

    return Pair(
        min(sortedList[it1].second, sortedList[it2].second),
        max(sortedList[it1].second, sortedList[it2].second)
    )
}

/**
 * Очень сложная (8 баллов)
 *
 * Входными данными является ассоциативный массив
 * "название сокровища"-"пара (вес сокровища, цена сокровища)"
 * и вместимость вашего рюкзака.
 * Необходимо вернуть множество сокровищ с максимальной суммарной стоимостью,
 * которые вы можете унести в рюкзаке.
 *
 * Перед решением этой задачи лучше прочитать статью Википедии "Динамическое программирование".
 *
 * Например:
 *   bagPacking(
 *     mapOf("Кубок" to (500 to 2000), "Слиток" to (1000 to 5000)),
 *     850
 *   ) -> setOf("Кубок")
 *   bagPacking(
 *     mapOf("Кубок" to (500 to 2000), "Слиток" to (1000 to 5000)),
 *     450
 *   ) -> emptySet()
 */

fun bagPacking(treasures: Map<String, Pair<Int, Int>>, capacity: Int): Set<String> {
    val values = Array(treasures.size + 1) { Array(capacity + 1) { 0 } }

    var it = 1
    for ((weight, value) in treasures.values) {
        for (w in 0..capacity) {
            if (w >= weight && (value + values[it - 1][w - weight]) > values[it - 1][w])
                values[it][w] = value + values[it - 1][w - weight]
            else values[it][w] = values[it - 1][w]
        }
        it++
    }


    // Get picked treasures
    val treasureList = treasures.toList()
    val pickedTreasures = mutableSetOf<String>()
    it--
    var w = capacity

    while (it != 0 && w != 0) {
        if (values[it][w] != values[it - 1][w]) {
            // Current treasure was picked
            pickedTreasures.add(treasureList[it - 1].first)
            w -= treasureList[it - 1].second.first
        }
        it--
    }
    return pickedTreasures
}
